    _SET_DIAGNOSTIC_(self%id_Coscin,sum(Coscin(:)))
    _SET_DIAGNOSTIC_(self%id_relCoscin,sum(Coscin(:))/sum(Phy(:)))
!---
    Picos=0.0d0
    Do ib=1,8!,5
!      Picos(ib)=exp(-3.5*(0.45-self%log_ESD(ib))**2)*Phy(ib)

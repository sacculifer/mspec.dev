    _SET_DIAGNOSTIC_(self%id_total_grazing,-sum(grazing(:))/sum(Phy(:)))
    _SET_DIAGNOSTIC_(self%id_mix_grazing,-sum(mixgraz(:,2)*Phy(:))/sum(Phy(:)))

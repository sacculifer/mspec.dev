    _SET_DIAGNOSTIC_(self%id_PON,Sum(Phy(:)*Q_N(:))+D_N+(Zoo(1)+Zoo(2)+Zoo(3)*(1.0_rk+self%csrA_z-csr))*self%Zoo_N)

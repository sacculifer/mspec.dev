    Do ib=1,self%phyto_num
      _SET_DIAGNOSTIC_(self%id_sizespec(ib),Phy(ib))
    end do
!---
    _SET_DIAGNOSTIC_(self%id_mixl,mixl)
!---
    Do ib=1,self%phyto_num
      _SET_DIAGNOSTIC_(self%id_rel_growth(ib),rel_growthr(ib))
    end do
